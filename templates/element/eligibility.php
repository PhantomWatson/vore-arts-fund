<p>
    To apply for funding, you must assert that all of these are true.
</p>
<ul>
    <li>
        The applicant
        <ul>
            <li>
                is a resident of Muncie, Indiana
            </li>
            <li>
                is at least 18 years old
            </li>
            <li>
                has never been disqualified from receiving funding from the Vore Arts Fund (VAF) for violating its
                rules, nor is representing an organization that has been thus disqualified
            </li>
            <li>
                has not contributed more than $5,000 to the VAF
            </li>
            <li>
                is not a manager (current director or officer) of the VAF nor a representative of any entity
                owned or controlled by a manager
            </li>
            <li>
                is not a close family member of a substantial contributor or manager of the VAF
            </li>
        </ul>
    </li>
    <li>
        The project for which funding is requested
        <ul>
            <li>
                will result in the creation, presentation, performance, or teaching of visual art, music, or performing
                arts in Muncie, Indiana
            </li>
            <li>
                can be reasonably expected to generate enough money to fully repay the loan awarded to it within a year
                of receiving that funding
            </li>
            <li>
                does not support nor oppose any political candidates
            </li>
            <li>
                does not violate any laws
            </li>
            <li>
                does not promote violent crimes
            </li>
            <li>
                does not promote violence or harassment against people based on protected characteristics, such
                as ethnicity, nationality, sexual orientation, sex, gender, religion, age, or disability
            </li>
        </ul>
    </li>
</ul>
